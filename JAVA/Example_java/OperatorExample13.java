    class OperatorExample13
	{  
		public static void main(String args[])
		{  
			// = Assignment Operator
			int a=10,c=30;  
			int b=20;  
			// +=  , -=  , *= , /=  Compound Assignment Operator
			a+=4;//a=a+4   (a=10+4)  
			b-=4;//b=b-4     (b=20-4) 
			c*=5;//c=c*5   (c=30*5) 
			System.out.println(a);  
			System.out.println(b); 
			System.out.println(c);
		}
	}  