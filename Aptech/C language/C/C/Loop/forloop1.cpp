#include<stdio.h>
int main()
{
	 //  print 1 to 100 using for loop	 
	for(int i=1;i<=100;i++)
	    printf("\n %d",i);
	//  print A to Z using for loop
	for(char c='A';c<='Z';c++)
	    printf("\n %c",c);
	//  print Hello World 100 times   
	for(int i=1;i<=100;i++)
	    printf("\n Hello World");
	    
	 return 0;
}
