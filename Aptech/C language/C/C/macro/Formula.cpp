#include<stdio.h>
#define squareofsum(x,y) x*x+2*x*y+y*y //It's a formula (a+b)^2
int main()
{
	float a,b,r;  //Float to get decimal numbers with point
	printf("\n Enter the 1 st number:    ");
	scanf("%f",&a);
	printf("\n Enter the 2 nd number:    ");
	scanf("%f",&b);
	r=squareofsum(a,b);
	printf("\n The result is %.2f",r);
	return 0;
}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	

