# 15. Program to print lower and upper case of inputted string

input_string = input("Enter a string: ")

lowercase_string = input_string.lower()  # Convert to lowercase using the lower() method
uppercase_string = input_string.upper()  # Convert to uppercase using the upper() method

print("Lowercase:", lowercase_string)  # Print the lowercase version of the entered string
print("Uppercase:", uppercase_string)  # Print the uppercase version of the entered string

